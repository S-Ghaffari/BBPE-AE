import random

with open('hotmail.txt', 'r',encoding='UTF-8', errors='ignore') as file:
    lines = file.readlines()
b = random.sample(lines, k=8926)  #number of lines as needed

test_size = int(len(b) * 0.2)  # 20% for the test set
random.shuffle(b)  # Shuffle the lines randomly
test = b[:test_size]
train = b[test_size:]

# Save test and train sets to separate files
with open('test_hotmail.txt', 'w', errors='ignore') as file:
    file.writelines(test)

with open('train_hotmail.txt', 'w', errors='ignore') as file:
    file.writelines(train)
